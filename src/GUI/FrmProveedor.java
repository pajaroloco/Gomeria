    private void jBtnGuardarEnBaseDeDatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarEnBaseDeDatoActionPerformed
        // TODO add your handling code here:
        if (jTblMemoria.getRowCount() > 0) {
            int guardar = JOptionPane.showConfirmDialog(this, "SEGURO QUIERE GUARDAR EN LA BASE DE DATOS?", "GUARDAR", JOptionPane.YES_NO_OPTION);
            if (guardar == JOptionPane.YES_OPTION) {
                for (int i = 0; i < jTblMemoria.getRowCount(); i++) {
                    IO.Proveedor p = new IO.Proveedor();
                    p.setRut((String) jTblMemoria.getValueAt(i, 0));
                    p.setNombreCompania((String) jTblMemoria.getValueAt(i, 1));
                    p.setNombreContacto((String) jTblMemoria.getValueAt(i, 2));
                    p.setTelefono((String) jTblMemoria.getValueAt(i, 3));
                    p.setDireccion((String) jTblMemoria.getValueAt(i, 4));
                    p.setActivo(true);
                    Conexion.getInstance().guardar(p);
                }
                JOptionPane.showMessageDialog(this, "A GUARDADO CORRECTAMENTE", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
                vaciadoMemoria();
                vaciadoBaseDeDato();
                Iterator<IO.Proveedor> ItProveedor = Conexion.getInstance().getProveedores().ListProveedor().iterator();
                
                while (ItProveedor.hasNext()) {

                    modelo.addElement(ItProveedor.next());
                }
                cargar();
            }
        } else {
            JOptionPane.showMessageDialog(this, "NO HAY DATOS EN LA TABLA", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBtnGuardarEnBaseDeDatoActionPerformed

