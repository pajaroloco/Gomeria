    public void mostrar() {
        String matris[][] = new String[lista.size()][5];
        for (int i = 0; i < lista.size(); i++) {
            matris[i][0] = lista.get(i).getRut();
            matris[i][1] = lista.get(i).getNombreCompania();
            matris[i][2] = lista.get(i).getNombreContacto();
            matris[i][3] = lista.get(i).getTelefono();
            matris[i][4] = lista.get(i).getDireccion();
        }
        jTblMemoria.setModel(new javax.swing.table.DefaultTableModel(
                matris,
                new String[]{
                    "Rut", "Nombre de compañia", "Nombre de contacto", "Telefono", "Direccion"
                }
        ));
    }

    public void cargar() {
        DefaultTableModel mdl = (DefaultTableModel) jTblBaseDeDatos.getModel();
        Iterator<IO.Proveedor> ItProveedor = Conexion.getInstance().getProveedores().ListProveedor().iterator();
        while (ItProveedor.hasNext()) {
            Object[] fila = new Object[5];
            Proveedor aux = ItProveedor.next();
            fila[0] = aux;
            fila[1] = aux.getNombreContacto();
            fila[2] = aux.getTelefono();
            fila[3] = aux.getDireccion();
            fila[4] = aux.isActivo();
            mdl.addRow(fila);
          jTblBaseDeDatos.getColumnModel().getColumn(0).setPreferredWidth(0);
        }
    }

