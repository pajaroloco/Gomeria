    public void validaCI() {
        Scanner sc = new Scanner(System.in);
        String s = jTxtCIMemoria.getText();
        String calcula = "2987634";
        int total = 0;
        for (int i = 0; i < 7; i++) {
            int si = Integer.parseInt(s.substring(i, i + 1));
            int no = Integer.parseInt(calcula.substring(i, i + 1));
            total = total + (si * no);
        }
        int multiplo = 0;
        int digito = 0;
        multiplo = ((total / 10) * 10) + 10;
        digito = (multiplo - total);
        if (digito == Integer.parseInt(s.substring(7, 8))) {
            JOptionPane.showMessageDialog(this, "la Cedula de: " + " Es Correcta", "ValidaciÃ³n C.I", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "la Cedula de: " + " Es Incorrecto");
        }
    }
