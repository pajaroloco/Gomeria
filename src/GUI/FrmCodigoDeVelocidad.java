    public void cargar() {
        DefaultTableModel mdl = (DefaultTableModel) jTblBaseDeDatos.getModel();
        Iterator<IO.CodigoDeVelocidad> ItCodigoDeVelocidad = Conexion.getInstance().getCodigoDeVelocidades().ListCodigoDeVelocidad().iterator();
        while (ItCodigoDeVelocidad.hasNext()) {
            Object[] fila = new Object[2];
            CodigoDeVelocidad aux = ItCodigoDeVelocidad.next();
            fila[0] = aux;
            fila[1] = aux.getVelocidadKmH();
            mdl.addRow(fila);
        }
    }

