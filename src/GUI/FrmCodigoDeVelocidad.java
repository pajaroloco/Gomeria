    private void jBtnGuardarEnBaseDeDatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarEnBaseDeDatoActionPerformed
        // TODO add your handling code here:
        if (jTblMemoria.getRowCount() > 0) {
            int guardar = JOptionPane.showConfirmDialog(this, "SEGURO QUIERE GUARDAR EN LA BASE DE DATOS?", "GUARDAR", JOptionPane.YES_NO_OPTION);
            if (guardar == JOptionPane.YES_OPTION) {
                for (int i = 0; i < jTblMemoria.getRowCount(); i++) {
                    IO.CodigoDeVelocidad cdv = new IO.CodigoDeVelocidad();
                    cdv.setSimbVelocidad((String) jTblMemoria.getValueAt(i, 0));
                    cdv.setVelocidadKmH((String) jTblMemoria.getValueAt(i, 1));
                    Conexion.getInstance().guardar(cdv);
                }
                JOptionPane.showMessageDialog(this, "A GUARDADO CORRECTAMENTE", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
                vaciadoBaseDeDatos();
                vaciadoMemoria();
                Iterator<IO.CodigoDeVelocidad> ItCodigoDeVelocidad = Conexion.getInstance().getCodigoDeVelocidades().ListCodigoDeVelocidad().iterator();
                while (ItCodigoDeVelocidad.hasNext()) {
                    modelo.addElement(ItCodigoDeVelocidad.next());
                }
                cargar();
                try{
                    FrmProducto frm = (FrmProducto) hola;
                    frm.cargarCombo();
                    this.dispose();
                }catch(Exception e){
                    
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "NO HAY DATOS EN LA TABLA", "INFIRMACION", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBtnGuardarEnBaseDeDatoActionPerformed

