    private void jBtnGuardarEnTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarEnTablaActionPerformed
        // TODO add your handling code here:
        if (jTxtCodigoMemoria.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "FALTA CODIGO DE VELOCIDAD", "CODIGO", JOptionPane.ERROR_MESSAGE);
            jTxtCodigoMemoria.requestFocus();
        } else if (jTxtKmHMemoria.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "FALTA KM/H", "KM/H", JOptionPane.ERROR_MESSAGE);
            jTxtKmHMemoria.requestFocus();
        } else {
            IO.CodigoDeVelocidad c = new IO.CodigoDeVelocidad();
            c.setSimbVelocidad(jTxtCodigoMemoria.getText());
            c.setVelocidadKmH(jTxtKmHMemoria.getText());
            lista.add(c);
            mostrar();
            iniPant();
        }
    }//GEN-LAST:event_jBtnGuardarEnTablaActionPerformed

    private void jBtnActualizarMemoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnActualizarMemoriaActionPerformed
        // TODO add your handling code here:
        int filaSelec = jTblMemoria.getSelectedRow();
        if (filaSelec >= 0) {
            if (jTxtCodigoMemoria.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "FALTA CODIGO DE VELOCIDAD", "CODIGO", JOptionPane.ERROR_MESSAGE);
                jTxtCodigoMemoria.requestFocus();
            } else if (jTxtKmHMemoria.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "FALTA KM/H", "KM/H", JOptionPane.ERROR_MESSAGE);
                jTxtKmHMemoria.requestFocus();
            } else {
                int actualizar = JOptionPane.showConfirmDialog(this, "SEGURO QUIERE ACTUALIZAR?", "ACTUALIZAR", JOptionPane.YES_NO_OPTION);
                if (actualizar == JOptionPane.YES_OPTION) {
                    IO.CodigoDeVelocidad a = (IO.CodigoDeVelocidad) lista.get(jTblMemoria.getSelectedRow());
                    a.setSimbVelocidad(jTxtCodigoMemoria.getText());
                    a.setVelocidadKmH(jTxtKmHMemoria.getText());
                    mostrar();
                    iniPant();
                } else {
                    jTxtCodigoMemoria.requestFocus();
                    iniPant();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "NO HA SELECCIONADO NINGUNA FILA", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBtnActualizarMemoriaActionPerformed

